text = "Hello, World!"
# position 2 to 5 
print(text[2:5])  # llo - it seems that : works like a bridge between two num
# slice from start to position 5
print(text[:5])   # Hello
# slice from position 2 to the end
print(text[2:])   # llo, World!

# negativee index from -5 to -2
print(text[-5:-2])  # orl
